    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String password = Arrays.toString(txtPassword.getPassword());
        
        if (username.isEmpty() || password.isEmpty()){
            txtError.setText("Username and password cannot be blank!");
        }
        
        UserInfo ui = userService.authenticate(username, password);
        if (ui == null){
            txtError.setText("Username and password is not valid");
            return;
        }
        
        dispose();
        new NewJFrame().setVisible(true);
    }//GEN-LAST:event_btnLoginActionPerformed

